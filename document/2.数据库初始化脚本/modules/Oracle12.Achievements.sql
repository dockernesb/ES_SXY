DROP TABLE DT_ACHIEVEMENTS;
DROP TABLE DT_ACHIEVEMENTS_KPI;

-- 绩效考核
CREATE TABLE DT_ACHIEVEMENTS
(
  ID                  VARCHAR2(50) PRIMARY KEY,
  DEPT_ID             VARCHAR2(50),
  YEAR                VARCHAR2(50),
  DEPT_SCORE          NUMBER,
  CENTER_SCORE        NUMBER,
  STATUS              VARCHAR2(5),
  CREATE_ID           VARCHAR2(50),
  CREATE_TIME         DATE,
  UPDATE_ID           VARCHAR2(50),
  UPDATE_TIME         DATE
);
-- ADD COMMENTS TO THE TABLE 
COMMENT ON TABLE DT_ACHIEVEMENTS
  IS '绩效考核';
-- ADD COMMENTS TO THE COLUMNS 
COMMENT ON COLUMN DT_ACHIEVEMENTS.ID
  IS '主键';
COMMENT ON COLUMN DT_ACHIEVEMENTS.DEPT_ID
  IS '部门ID';
COMMENT ON COLUMN DT_ACHIEVEMENTS.YEAR
  IS '年度(YYYY)';
COMMENT ON COLUMN DT_ACHIEVEMENTS.DEPT_SCORE
  IS '部门自评总分';
COMMENT ON COLUMN DT_ACHIEVEMENTS.CENTER_SCORE
  IS '中心评分总分';
COMMENT ON COLUMN DT_ACHIEVEMENTS.STATUS
  IS '状态（1：部门自评，2：待评分，3：已评分）';
COMMENT ON COLUMN DT_ACHIEVEMENTS.CREATE_ID
  IS '创建人';
COMMENT ON COLUMN DT_ACHIEVEMENTS.CREATE_TIME
  IS '创建时间';
COMMENT ON COLUMN DT_ACHIEVEMENTS.UPDATE_ID
  IS '更新人';
COMMENT ON COLUMN DT_ACHIEVEMENTS.UPDATE_TIME
  IS '更新时间';

-- 绩效考核指标得分
CREATE TABLE DT_ACHIEVEMENTS_KPI
(
  ID                  VARCHAR2(50) PRIMARY KEY,
  ACHIEVEMENTS_ID     VARCHAR2(50),
  KPI_CODE            VARCHAR2(50),
  KPI_ITEM_CODE       VARCHAR2(50),
  DEPT_SCORE          NUMBER,
  DEPT_DESC           VARCHAR2(500),
  CENTER_SCORE        NUMBER,
  CENTER_DESC         VARCHAR2(500)
);
-- ADD COMMENTS TO THE TABLE 
COMMENT ON TABLE DT_ACHIEVEMENTS_KPI
  IS '绩效考核指标得分';
-- ADD COMMENTS TO THE COLUMNS 
COMMENT ON COLUMN DT_ACHIEVEMENTS_KPI.ID
  IS '主键';
COMMENT ON COLUMN DT_ACHIEVEMENTS_KPI.ACHIEVEMENTS_ID
  IS '绩效考核表ID';
COMMENT ON COLUMN DT_ACHIEVEMENTS_KPI.KPI_CODE
  IS '指标编码';
COMMENT ON COLUMN DT_ACHIEVEMENTS_KPI.KPI_ITEM_CODE
  IS '指标项编码';
COMMENT ON COLUMN DT_ACHIEVEMENTS_KPI.DEPT_SCORE
  IS '部门自评分';
COMMENT ON COLUMN DT_ACHIEVEMENTS_KPI.CENTER_SCORE
  IS '中心评分';
COMMENT ON COLUMN DT_ACHIEVEMENTS_KPI.CENTER_DESC
  IS '中心备注';

